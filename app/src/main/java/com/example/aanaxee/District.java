package com.example.aanaxee;

public class District {
    public String name;
    public int did;

    public District() {
        this.name = name;
        this.did = did;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    public int getSid() {
        return did;
    }

    public void setSid(int did) {
        this.did = did;
    }
}
