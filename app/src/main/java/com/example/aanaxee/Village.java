package com.example.aanaxee;

public class Village {
    public String name;
    public int vid;

    public Village() {
        this.name = name;
        this.vid = vid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getVid() {
        return vid;
    }

    public void setVid(int vid) {
        this.vid = vid;
    }

}
